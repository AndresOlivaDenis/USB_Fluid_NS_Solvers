    (dx_wP(2:Nx+1,2:Ny+1)+dx_Pe(2:Nx+1,2:Ny+1)).*... % bubble Area
    (dy_Pn(2:Nx+1,2:Ny+1)+dy_sP(2:Nx+1,2:Ny+1))));
da=(4/pi*Ab)^0.5;    CirBubble(it+1,1)=pi*da/Pb;
